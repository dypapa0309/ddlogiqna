  function escapeHtml(s) {
    return String(s)
      .replaceAll("&", "&amp;")
      .replaceAll("<", "&lt;")
      .replaceAll(">", "&gt;")
      .replaceAll('"', "&quot;")
      .replaceAll("'", "&#039;");
  }

  function renderFaqItem(item) {
    const id = `faq-${item.id}`;
    const keywords = (item.tags || []).join(" ");

    const blocks = item.a.map(([title, body]) => {
      // body가 배열/문자열 둘 다 지원
      if (Array.isArray(body)) {
        const lis = body.map((x) => `<li>${escapeHtml(x)}</li>`).join("");
        return `<p><b>${escapeHtml(title)}</b></p><ul>${lis}</ul>`;
      }
      return `<p><b>${escapeHtml(title)}</b><br>${escapeHtml(body).replaceAll("\n", "<br>")}</p>`;
    }).join("");

    const tagsHtml = (item.tags && item.tags.length)
      ? `<div class="tag-row">${item.tags.map(t => `<span class="tag">${escapeHtml(t)}</span>`).join("")}</div>`
      : "";

    return `
      <div class="faq-item" data-id="${id}" data-keywords="${escapeHtml(keywords)}">
        <button class="faq-question" id="${id}" type="button" aria-expanded="false">
          <span class="q-num">${escapeHtml(item.id)}</span>
          <span class="q-text">${escapeHtml(item.q)}</span>
          <span class="q-icon">+</span>
        </button>
        <div class="faq-answer" role="region" aria-hidden="true">
          ${blocks}
          ${tagsHtml}
        </div>
      </div>
    `;
  }

  function setExpanded(btn, expanded) {
    const answer = btn.nextElementSibling;
    btn.setAttribute("aria-expanded", expanded ? "true" : "false");
    if (answer) {
      answer.setAttribute("aria-hidden", expanded ? "false" : "true");
      answer.style.display = expanded ? "block" : "none";
    }
    const icon = btn.querySelector(".q-icon");
    if (icon) icon.textContent = expanded ? "–" : "+";
  }

  function scrollToBtn(btn) {
    const stickyOffset = 78;
    const y = btn.getBoundingClientRect().top + window.scrollY - stickyOffset;
    window.scrollTo({ top: y, behavior: "smooth" });
  }

  document.addEventListener("DOMContentLoaded", () => {
    const faqList = $("#faqList");
    const searchInput = $("#searchInput");
    const expandAllBtn = $("#expandAllBtn");
    const collapseAllBtn = $("#collapseAllBtn");
    const resultCount = $("#resultCount");

    if (!faqList) return;

    // 1) 렌더
    faqList.innerHTML = FAQS.map(renderFaqItem).join("");

    const allItems = () => $$(".faq-item", faqList);
    const allQuestions = () => $$(".faq-question", faqList);

    // 2) 초기 접기
    allQuestions().forEach(btn => setExpanded(btn, false));
    resultCount.textContent = `${allItems().length}개 항목`;

    // 3) 토글 (단일 오픈 아코디언)
    faqList.addEventListener("click", (e) => {
      const btn = e.target.closest(".faq-question");
      if (!btn) return;

      const isOpen = btn.getAttribute("aria-expanded") === "true";

      // 다른 항목 닫기
      allQuestions().forEach(b => {
        if (b !== btn) setExpanded(b, false);
      });

      // 현재 토글
      setExpanded(btn, !isOpen);
      if (!isOpen) scrollToBtn(btn);
    });

    // 4) 검색
    const applyFilter = () => {
      const q = (searchInput?.value || "").trim().toLowerCase();
      let visible = 0;

      allItems().forEach(item => {
        const text = (item.innerText || "").toLowerCase();
        const keywords = (item.getAttribute("data-keywords") || "").toLowerCase();
        const hit = !q || text.includes(q) || keywords.includes(q);

        item.style.display = hit ? "block" : "none";
        if (hit) visible++;

        // 검색 중엔 닫기
        const btn = item.querySelector(".faq-question");
        if (btn) setExpanded(btn, false);
      });

      resultCount.textContent = `${visible}개 항목`;
    };

    if (searchInput) searchInput.addEventListener("input", applyFilter);

    // 5) 전체 펼치기/접기
    if (expandAllBtn) {
      expandAllBtn.addEventListener("click", () => {
        allItems().forEach(item => {
          if (item.style.display === "none") return;
          const btn = item.querySelector(".faq-question");
          if (btn) setExpanded(btn, true);
        });
      });
    }

    if (collapseAllBtn) {
      collapseAllBtn.addEventListener("click", () => {
        allQuestions().forEach(btn => setExpanded(btn, false));
      });
    }

    // 6) 긴급 버튼: 해당 항목 열고 이동
    $$(".urgent-btn").forEach(u => {
      u.addEventListener("click", () => {
        const targetId = u.getAttribute("data-open");
        if (!targetId) return;

        // 검색어 초기화(숨겨져 있으면 안 보이니까)
        if (searchInput) {
          searchInput.value = "";
          applyFilter();
        }

        const targetBtn = document.getElementById(targetId);
        if (!targetBtn) return;

        allQuestions().forEach(b => setExpanded(b, b === targetBtn));
        scrollToBtn(targetBtn);
      });
    });
  });
